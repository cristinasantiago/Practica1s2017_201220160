    private void abrirArchivoXML() {
        //Declaracion e instanciacion de JFileChooser
        JFileChooser chooser = new JFileChooser();
        //Filtro para que admita solo extensiones .xml
        FileNameExtensionFilter filter = new FileNameExtensionFilter("XML files", "xml");
        chooser.setFileFilter(filter);
        //Se muestra el fileChooser
        int returnVal = chooser.showOpenDialog(this);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            //Se envia el archivo leido a la carga de XML
            cargarArchivoXML(chooser.getSelectedFile());
        }
        btnJugar.setEnabled(true);
    }

    private void cargarArchivoXML(File inputFile) {
        try {
            //se declara e instacia el parser de SAX
            SAXParserFactory factory = SAXParserFactory.newInstance();
            SAXParser saxParser = factory.newSAXParser();
            //se instancia un handler que se ha declaro de manera global para que exista en todo el codigo de esta clase

            //se parsea el xml
            saxParser.parse(inputFile, mihandler);
            //ahora nuestro xml ya esta parseado y se encuentra en la variable "mihandler"
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            e.printStackTrace();
        }
    }
